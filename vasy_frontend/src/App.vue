  <router-view />
  <div
    id="pagetop"
    class="fixed-bottom"
    style="left: 87%"
    v-show="scY > 300"
    @click="toTop"
  >
    <i class="fa-solid fa-circle-arrow-up fa-3x mb-2 me-5"></i>
  </div>
</template>

<script>
export default {
  name: "App-Component",
  el: "#app",
  data() {
    return {
      scTimer: 0,
      scY: 0,
    };
  },
  mounted() {
    window.addEventListener("scroll", this.handleScroll);
  },
  methods: {
    handleScroll: function () {
      if (this.scTimer) return;
      this.scTimer = setTimeout(() => {
        this.scY = window.scrollY;
        clearTimeout(this.scTimer);
        this.scTimer = 0;
      }, 100);
    },
    toTop: function () {
      window.scrollTo({
        top: 0,
        behavior: "smooth",
      });
    },
  },
};
</script>

